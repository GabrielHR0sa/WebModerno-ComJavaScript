const a = 7;
let b = 3;

b += a; // atribuição aditiva, acrescentando valor de a ao b / b = b + a
console.log(b);

b -= 4; // atribuição subtrativa, subtrai 4 do valor de b / b = b - 4
console.log(b);

b *= 2; // b = b * 2
console.log(b);

 b /= 2; // b = b / 2
 console.log(b);


 b %= 2; // b = b % 2
 console.log(b);